package com.org.devops;


public class Calculator {

	public int addNumbers(int i, int j) {
		return i+j;
	}

	public int substractNumber(int i, int j) {
		return i-j;
	}

	public int multiplyNumber(int i, int j) {
		return i*j;
	}

	public int divideNumber(int i, int j) {
		return i/j;
	}

}
